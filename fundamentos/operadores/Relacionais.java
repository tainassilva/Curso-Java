package fundamentos.operadores;

public class Relacionais {
	public static void main(String[] args) {
		
		System.out.println(3>4); // 3 é maior que 4?
		System.out.println(3>=3); // 3 é maior ou igual a 3?
		System.out.println(3<7); //3 é menor que 7?
		System.out.println(30<=7); // 30 é menor ou igual a 7?
		System.out.println(30!=7); // 30 é diferente de 7?
		
	}
}
